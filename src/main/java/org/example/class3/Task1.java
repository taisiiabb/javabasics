package org.example.class3;

public class Task1 {
    public static void main(String[] args) {
        float firstNum = 10.0f;
        float secondNum = 20.0f;

        float addition = firstNum + secondNum;
        float subtraction = firstNum - secondNum;
        float multiplication = firstNum * secondNum;
        float division = firstNum / secondNum;

        System.out.println("The addition of 2 numbers "+ firstNum +" and "+
                secondNum +" is equal to "+ addition);
        System.out.println("The subtraction of 2 numbers "+ firstNum +" and "+
                secondNum +" is equal to "+ subtraction);
        System.out.println("The multiplication of 2 numbers "+ firstNum +" and "+
                secondNum +" is equal to "+ multiplication);
        System.out.println("The division of 2 numbers "+ firstNum +" and "+
                secondNum +" is equal to "+ division);


    }
}
